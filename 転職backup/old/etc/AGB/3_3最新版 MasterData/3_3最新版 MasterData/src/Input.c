//**************************************************************************** S
//	  関数名	:	KeyManagaer
//	   機能		:	フレーム毎のキー入力状況を取得する
//	   引数 	:	無し
//	  戻り値	:	無し
//-----------------------------------------------------------------------------
//	 コメント	:	ほぼ毎フレーム使用される関数
//	最終更新日	:	2002/03/03 (Mon)
//**************************************************************************** E
void	KeyManager( void )
{
	OldKey = Key;
	Key = (*(vu16 *)REG_KEYINPUT ^ ALL_KEY_MASK);
	Trg = Key & (Key ^ OldKey);

	return;
}
